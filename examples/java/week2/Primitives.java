class Primitives {
    public static void main(String[] args) {
        // Using the String class rather than char since it is a lot easier
        // and give more flexibility
        String H = "H";
        int three = 3;
        int one_1 = 1;
        int one_2 = 1;
        int zero_1 = 0;
        String w = "w";
        int zero_2 = 0;
        String r = "r";
        int one_3 = 1;
        String d = "d";
        float two = (float) 2.0;
        boolean t = true;
        System.out.println(H + three + one_1 + one_2 + zero_1 + w + zero_2 + r + one_3 + d + two + t);
    }
}
